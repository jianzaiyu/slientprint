public class ZebraConfigs {

    private String zebraCode;
    private String zebraPrinterIp;
    private String zebraPrinterPort;
    private String zebraPrinterName;

    public String getZebraCode() {
        return zebraCode;
    }

    public void setZebraCode(String zebraCode) {
        this.zebraCode = zebraCode;
    }

    public String getZebraPrinterIp() {
        return zebraPrinterIp;
    }

    public void setZebraPrinterIp(String zebraPrinterIp) {
        this.zebraPrinterIp = zebraPrinterIp;
    }

    public String getZebraPrinterPort() {
        return zebraPrinterPort;
    }

    public void setZebraPrinterPort(String zebraPrinterPort) {
        this.zebraPrinterPort = zebraPrinterPort;
    }

    public String getZebraPrinterName() {
        return zebraPrinterName;
    }

    public void setZebraPrinterName(String zebraPrinterName) {
        this.zebraPrinterName = zebraPrinterName;
    }


}
